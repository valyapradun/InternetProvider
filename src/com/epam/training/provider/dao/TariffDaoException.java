package com.epam.training.provider.dao;

public class TariffDaoException extends Exception {

	private static final long serialVersionUID = 8972829450088503692L;

	public TariffDaoException() {
		super();
	}

	public TariffDaoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TariffDaoException(String arg0) {
		super(arg0);
	}

	public TariffDaoException(Throwable arg0) {
		super(arg0);
	}

}
